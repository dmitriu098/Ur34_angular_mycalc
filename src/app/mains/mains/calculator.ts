export class Calculator {
      constructor(){

      }

      sum(arg1, arg2){
        return Number(arg1) + Number(arg2);
      }

      differ(arg1, arg2){
        return Number(arg1) - Number(arg2);
      }

      mult(arg1, arg2){
        return Number(arg1) * Number(arg2);
      }

      divs(arg1, arg2){
        return Number(arg1) / Number(arg2);
      }



}
